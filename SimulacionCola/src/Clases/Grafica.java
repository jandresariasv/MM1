package Clases;

public class Grafica {
    
    private double en_cola;
    private double tiempo;
    private double cliente;
    private double en_sistema;

    public double getEn_sistema() {
        return en_sistema;
    }

    public void setEn_sistema(double en_sistema) {
        this.en_sistema = en_sistema;
    }
    
    public double getEn_cola() {
        return en_cola;
    }

    public void setEn_cola(double en_cola) {
        this.en_cola = en_cola;
    }

    public double getTiempo() {
        return tiempo;
    }

    public void setTiempo(double tiempo) {
        this.tiempo = tiempo;
    }

    public double getCliente() {
        return cliente;
    }

    public void setCliente(double cliente) {
        this.cliente = cliente;
    }
    
}